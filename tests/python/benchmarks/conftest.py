"""
tests/python/benchmarks/conftest.py

Hooks and fixtures pytest-benchmark.

Contains :
  - Hook pytest_benchmark_update_machine_info : enrich machine_info with
    CPU flags (AVX/SSE4/FMA), Frequency, caches L2/L3, available RAM
"""
from __future__ import annotations

import os
import platform
import subprocess

import psutil

# ---------------------------------------------------------------------------
# Hook : enrich machine_info of every pytest-benchmark JSON
# ---------------------------------------------------------------------------


def pytest_benchmark_update_machine_info(config, machine_info: dict) -> None:
    """
    Automatically called by pytest-benchmark before each session.
    Adds CPU flags (AVX/SSE4/FMA), Frequency, caches L2/L3, available RAM,
    runner.
    Added informations are stored in the machine_info section dict of the JSON
    generated by --benchmark-save.
    """
    try:
        from cpuinfo import get_cpu_info

        cpu = get_cpu_info()
        relevant = {"avx", "avx2", "avx512f", "sse4_1", "sse4_2", "fma", "aes"}
        machine_info["cpu_flags"] = [f for f in cpu.get("flags", []) if f in relevant]
        machine_info["cpu_hz_actual"] = cpu.get("hz_actual_friendly", "?")
        machine_info["cpu_l2_cache"] = cpu.get("l2_cache_size")
        machine_info["cpu_l3_cache"] = cpu.get("l3_cache_size")
    except ImportError:
        machine_info["cpu_flags"] = []
        machine_info["cpu_hz_actual"] = "? (pip install py-cpuinfo)"

    vm = psutil.virtual_memory()
    machine_info["memory_available_gb"] = round(vm.available / 1e9, 2)
    machine_info["swap_total_gb"] = round(psutil.swap_memory().total / 1e9, 2)

    machine_info["bench_runner"] = (
        os.environ.get("BENCH_RUNNER") or os.environ.get("CI_RUNNER_DESCRIPTION") or platform.node()
    )
    machine_info["bench_runner_id"] = os.environ.get("CI_RUNNER_ID", "local")

    machine_info["lib_version"] = os.environ.get("BENCH_VERSION", "")
    machine_info["git_commit"] = os.environ.get("BENCH_COMMIT", _git_commit())
    machine_info["git_dirty"] = os.environ.get("BENCH_DIRTY", "0") == "1"


def _git_commit() -> str:
    try:
        return (
            subprocess.check_output(
                ["git", "rev-parse", "--short", "HEAD"], stderr=subprocess.DEVNULL
            )
            .decode()
            .strip()
        )
    except Exception:
        return "unknown"
